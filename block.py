import hashlib


#
class Block:

    #
    def __init__(self, previous_hash, vote):
        self.vote = vote
        self.previous_hash = previous_hash
        string_to_hash = "".join(vote) + previous_hash
        self.block_hash = hashlib.sha256(string_to_hash.encode()).hexdigest()

